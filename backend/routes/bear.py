_cache: dict = {"lines": _FALLBACK, "ts": 0.0}
_lock = threading.Lock()
_TTL = 20 * 60  # 20 分鐘換一批


def _generate() -> list[str]:
    api_key = os.getenv("ANTHROPIC_API_KEY", "")
    if not api_key:
        return []
    try:
        import anthropic
        client = anthropic.Anthropic(api_key=api_key)
        msg = client.messages.create(
            model="claude-haiku-4-5-20251001",
            max_tokens=512,
            messages=[{"role": "user", "content": "請生成 10 條台詞"}],
            system=_SYSTEM_PROMPT,
        )
        raw = msg.content[0].text.strip()
        lines = [l.strip() for l in raw.splitlines() if l.strip()]
        return lines if len(lines) >= 5 else []
    except Exception:
        return []


def _refresh():
    lines = _generate()
    if lines:
        with _lock:
            _cache["lines"] = lines
            _cache["ts"] = time.time()


def _bg_worker():
    while True:
        time.sleep(_TTL)
        _refresh()


# 啟動背景更新執行緒
_t = threading.Thread(target=_bg_worker, daemon=True)
_t.start()


@router.get("/bear-lines")
def get_bear_lines():
    # 首次或快取過期就即時生成一次（non-blocking fallback）
    with _lock:
        age = time.time() - _cache["ts"]
        lines = list(_cache["lines"])

    if age > _TTL:
        import concurrent.futures
        with concurrent.futures.ThreadPoolExecutor(max_workers=1) as ex:
            future = ex.submit(_generate)
            try:
                new = future.result(timeout=8)
                if new:
                    with _lock:
                        _cache["lines"] = new
                        _cache["ts"] = time.time()
                    lines = new
            except Exception:
                pass

    return {"lines": lines}
